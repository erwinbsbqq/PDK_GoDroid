void ali_audio_test_task(struct work_struct *work)
{
	UINT32 cnt=0;
	UINT8 *aud_buf;
	UINT32 buf_len, cur,data;
	struct deca_device *deca_dev;
	struct control_block cb;
	
	PRINTK_INFO("Enter %s.\n",__FUNCTION__);

    data = *((unsigned long *)0xb8001288);
    data = 0x00077000;
    *((unsigned long *)0xb8001288) = data;
    *((unsigned long *)0xb8001294) = data;

    // enable mem gpio, share pin with DDR2 XDQ
    *((UINT32 *)0xb80012a4) = 0x3ffff;

    *((UINT32*)0xb8000430)=0xFF0001FF;
    *((UINT32*)0xb8000434)=0xF8FFEFFF;
    *((UINT32*)0xb8000438)=0xF7FFFFFF;
    *((UINT32*)0xb800043c)=0;
    


	while(hold_test_task)
	{
		PRINTK_INFO("test task hold: %d\n", cnt);
		msleep(1000);
	}

	ali_test_fop->open(NULL, &ali_test_file);

	ali_test_fop->ioctl(NULL, &ali_test_file, AUDIO_SET_STREAMTYPE, AUDIO_MPEG2);

	
	ali_test_fop->ioctl(NULL, &ali_test_file, AUDIO_SET_VOLUME, 100);
	
	ali_test_fop->ioctl(NULL, &ali_test_file, AUDIO_CHANNEL_SELECT, SND_DUP_L);
	
	deca_dev=((struct ali_audio_device*)ali_test_file.private_data)->deca_dev;
	
	ali_test_fop->ioctl(NULL, &ali_test_file, AUDIO_SET_AV_SYNC, ADEC_SYNC_FREERUN);
	ali_test_fop->ioctl(NULL, &ali_test_file, AUDIO_PLAY, 0);
	
	cur=0;
	memset(&cb, 0, sizeof(struct control_block));
	while(1)
	{
		//PRINTK_INFO("test task: %d\n", cnt);
		//deca_request_write(deca_dev, 384, &aud_buf, &buf_len, &cb);
		if(RET_SUCCESS==deca_request_write(deca_dev, 384, &aud_buf, &buf_len, &cb))
		{
			//if((*((volatile UINT8*)0xb800206e))==0)
			if(buf_len==0)
			{
				msleep(20);
				continue;
			}
			
			if(cur+buf_len<beep_bin_array_size)
			{
				//memcpy(aud_buf, beep_bin_array+cur, buf_len);
				hld_dev_memcpy(aud_buf, beep_bin_array+cur, buf_len);
				cur+=buf_len;
			}
			else
			{
				//memcpy(aud_buf, beep_bin_array+cur, beep_bin_array_size-cur);
				hld_dev_memcpy(aud_buf, beep_bin_array+cur, beep_bin_array_size-cur);
				buf_len=beep_bin_array_size-cur;
				cur=0;
			}
			deca_update_write(deca_dev, buf_len);
			//msleep(10);
			//PRINTK_INFO("update audio data: %d\n", buf_len);
			//cnt++;
			//printk("%d\n", buf_len);
		}
		else
		{
			msleep(20);
		}
	}
}

