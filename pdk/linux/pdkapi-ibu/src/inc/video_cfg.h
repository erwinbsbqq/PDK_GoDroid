typedef struct VideoCfg_t
{
	UINT32 input_idx;
	UINT32 output_idx;

/* Common state encoding in a 32-bit integer :
 *   bits  
 *         config for b frame:
 *
 *		   0-1    vpp_effort   
 *         2      scaler_src
 *		   3-4    field_src
 *         5-6  unused
 *         7    dv_mode
 *		   8-9  dv_v_scale_factor
 *		   10-11  dv_h_scale_factor
 *		   12   dv_scale_enable
 *		   13-14 h_scale_factor
 *		   15	 h_scale_enable  
 *     
 *			config for I/p frame:
 *
 *		   16-31    map as bit0-bit15
 */
	UINT32 de_n_cfg;
	UINT32 de_o_cfg;

}VideoCfg, *pVideoCfg;


