#define VGETVDVFACTOR(a)		((a&(0x3<<8))>>8)
#define VGETDVMODE(a)		((a&(0x1<<7))>>7)

#define VGETFIELDSRC(a)		((a&(0x3<<3))>>3)
#define VGETSCALESRC(a)		((a&(0x1<<2))>>2)
#define VGETVPPEFFORT(a)		(a&0x3)

#define VSETCFGVALUE(a,b)		a->h_scale_enable = VGETHEN(b);	\
								a->h_scale_factor = VGETHFACTOR(b); \
								a->dview_enable = VGETDVEN(b); \
								a->dv_h_scale_factor = VGETHDVFACTOR(b); \
								a->dv_v_scale_factor = VGETVDVFACTOR(b); \
								a->dv_mode = VGETDVMODE(b); \
								a->field_src = VGETFIELDSRC(b) ;\
								a->scaler_src = VGETSCALESRC(b);\
								a->vpp_effort = VGETVPPEFFORT(b)


